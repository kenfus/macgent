class AgentBrowser:
    """Python wrapper for the agent-browser CLI with stealth features."""
    
    def __init__(self, config: StealthConfig | None = None):
        self.config = config or StealthConfig()
        self._browser_path = self._find_agent_browser()
        self._config_file: Path | None = None
        self._is_open = False
        self._current_url: str | None = None
        
    def _find_agent_browser(self) -> str:
        """Find the agent-browser CLI executable."""
        # Check common locations
        candidates = [
            shutil.which("agent-browser"),
            os.path.expanduser("~/.npm-global/bin/agent-browser"),
            "/usr/local/bin/agent-browser",
            "/opt/homebrew/bin/agent-browser",
        ]
        for path in candidates:
            if path and Path(path).exists():
                logger.debug(f"Found agent-browser at: {path}")
                return path
        raise RuntimeError("agent-browser CLI not found. Install with: npm install -g agent-browser")
    
    def _write_config_file(self) -> Path:
        """Write a temporary config file for agent-browser."""
        config_data = self.config.to_agent_browser_config()
        config_file = Path(tempfile.mktemp(suffix=".json", prefix="agent-browser-config-"))
        config_file.write_text(json.dumps(config_data, indent=2))
        logger.debug(f"Wrote config file: {config_file}")
        return config_file
    
    def _run_command(self, *args: str, json_output: bool = False, timeout: float = 30.0) -> str:
        """Run an agent-browser command and return the output."""
        cmd = [self._browser_path]
        
        # Add config file if we have one
        if self._config_file:
            cmd.extend(["--config", str(self._config_file)])
        
        # Add JSON output flag if requested
        if json_output:
            cmd.append("--json")
        
        cmd.extend(args)
        
        logger.debug(f"Running: {' '.join(cmd)}")
        
        try:
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=timeout,
            )
            if result.returncode != 0:
                error_msg = result.stderr.strip() or result.stdout.strip()
                logger.error(f"agent-browser error: {error_msg}")
                raise RuntimeError(f"agent-browser failed: {error_msg}")
            return result.stdout.strip()
        except subprocess.TimeoutExpired:
            raise RuntimeError(f"agent-browser command timed out: {' '.join(args)}")
    
    def start(self) -> "AgentBrowser":
        """Start the browser session with stealth configuration.

        Always kills any existing daemon first so our stealth args (user-agent,
        --disable-blink-features=AutomationControlled, etc.) are actually applied.
        The daemon silently ignores args when reusing an existing session, which
        leaves navigator.webdriver=true and gets us instantly detected.
        """
        # Kill existing daemon so our args aren't silently ignored
        subprocess.run([self._browser_path, "close"], capture_output=True)
        self._config_file = self._write_config_file()
        logger.info(f"Started agent-browser session with stealth config")
        logger.info(f"User-Agent: {self.config.get_user_agent()}")
        return self
    
    def open(self, url: str, extra_headers: dict[str, str] | None = None) -> str:
        """Navigate to a URL with realistic headers.
        
        Args:
            url: URL to navigate to
            extra_headers: Additional headers to merge with generated headers
        """
        if not url.startswith(("http://", "https://")):
            url = f"https://{url}"
        
        # Get realistic headers from browserforge
        all_headers = self.config.get_headers()
        
        # Merge any extra headers
        if extra_headers:
            all_headers = {**all_headers, **extra_headers}
        
        # Build the open command with headers
        args = ["open", url, "--headers", json.dumps(all_headers)]
        
        # Add user-agent
        user_agent = self.config.get_user_agent()
        if user_agent:
            args.extend(["--user-agent", user_agent])
        
        # Add headed mode
        if self.config.headed:
            args.append("--headed")
        
        # Add browser args
        if self.config.browser_args:
            args.extend(["--args", ",".join(self.config.browser_args)])
        
        result = self._run_command(*args, timeout=60.0)
        self._is_open = True
        self._current_url = url
        logger.info(f"Opened: {url}")
        return result
    
    def inject_fingerprint_spoof(self) -> str:
        """Inject JavaScript to spoof browser fingerprint."""
        return self.eval_js(FINGERPRINT_SPOOF_JS)
    
    def snapshot(self, interactive: bool = False) -> dict[str, Any]:
        """Get the accessibility tree snapshot for AI agents."""
        args = ["snapshot"]
        if interactive:
            args.append("-i")
        result = self._run_command(*args, json_output=True, timeout=30.0)
        return json.loads(result) if result else {}
    
