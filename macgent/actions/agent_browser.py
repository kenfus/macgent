@dataclass
class StealthConfig:
    """Configuration for stealth browser settings."""
    
    browser_name: str = "chrome"
    browser_min_version: int = 120
    os_name: str = "macos"
    locale: list[str] = field(default_factory=lambda: ["en-US", "de-CH", "de", "en"])
    http_version: int = 2
    browser_args: list[str] = field(default_factory=lambda: STEALTH_BROWSER_ARGS)
    headed: bool = True  # Headed mode is harder to detect
    profile_path: str | None = None
    proxy: str | None = None
    color_scheme: str = "light"
    
    # Generated headers (cached)
    _headers: dict[str, str] | None = field(default=None, repr=False)
    _user_agent: str | None = field(default=None, repr=False)
    
    def get_headers(self) -> dict[str, str]:
        """Get realistic headers, generating them if needed."""
        if self._headers is None:
            self._headers = generate_realistic_headers(
                browser_name=self.browser_name,
                min_version=self.browser_min_version,
                os_name=self.os_name,
                locale=self.locale,
                http_version=self.http_version,
            )
        return self._headers
    
    def get_user_agent(self) -> str:
        """Get the User-Agent string from generated headers."""
        if self._user_agent is None:
            headers = self.get_headers()
            self._user_agent = headers.get("User-Agent", "")
        return self._user_agent
    
    def to_agent_browser_config(self) -> dict[str, Any]:
        """Convert to agent-browser config file format."""
        config: dict[str, Any] = {
            "headed": self.headed,
            "userAgent": self.get_user_agent(),
            "colorScheme": self.color_scheme,
        }
        if self.profile_path:
            config["profile"] = self.profile_path
        if self.proxy:
            config["proxy"] = self.proxy
        if self.browser_args:
            config["args"] = ",".join(self.browser_args)
        return config


