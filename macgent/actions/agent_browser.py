def generate_realistic_headers(
    browser_name: str = "chrome",
    min_version: int = 120,
    os_name: str = "macos",
    locale: list[str] | None = None,
    http_version: int = 2,
) -> dict[str, str]:
    """Generate realistic browser headers using browserforge.
    
    Args:
        browser_name: Browser name (chrome, firefox, safari, edge)
        min_version: Minimum browser version
        os_name: Operating system (macos, windows, linux)
        locale: List of locale preferences
        http_version: HTTP version (1, 2)
        
    Returns:
        Dictionary of realistic headers
    """
    if locale is None:
        # Based on user's timezone (Europe/Zurich)
        locale = ["en-US", "de-CH", "de", "en"]
    
    generator = HeaderGenerator(
        browser=Browser(name=browser_name, min_version=min_version),
        os=os_name,
        locale=locale,
        http_version=http_version,
    )
    return generator.generate()


